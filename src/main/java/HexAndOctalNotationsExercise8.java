/**
 * Show that hex and octal notations work with long values. Use
 * Long.toBinaryString( ) to display the results.
 */
public class HexAndOctalNotationsExercise8 {
    private int a;
    private int b;

    public HexAndOctalNotationsExercise8(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
